                            <h1 class="mb-3 text-secondary text-uppercase font-weight-bold">PH actors in international films share best practices of foreign counterparts</h1>
                            
                            <p> For actor Soliman Cruz, who has been receiving rave reviews worldwide for playing the conflicted seafarer Joel in the Romanian film “To the North,” being the unofficial ambassador of all Filipino seafarers is a tall order. <p>

                              <p> “I’ve been approached by a number of people to say that I remind them of their fathers or relatives who also worked as a bosun, the rank of my character in the film, and that I am now the representative of these people to the world. I never knew how to respond to their comments. I guess it’s because I still don’t understand what’s unfolding,” the acclaimed actor said. <p>
                              
                              <p> A bosun is like a ship’s foreman, explained Cruz. He looks over the crew and equipment on the ship. “I will not claim to know everything about what is happening in our maritime industry, but my wish is that we will be able to improve the capabilities of our work force to the level that will match international standards,” he declared. <p>
                              
                              <p> Speaking of standards, we asked Cruz to share some of the best practices he has observed while shooting the film in Romania that he hoped would also be done locally. To this, Cruz said: “Rehearsals prior to shooting is important to them. It’s when they study the script, it’s structure and dramaturgy. We rehearsed for a week. Another week was allotted for rehearsals with the cameras and lights. When it came to the actual shoot, everything went smoothly. As soon as we’re done for the day, it was already vodka time!” <p>
                              
                              <p> “To the North,” directed by Mihai Mincan, is the closing film of this year’s edition of the QCinema International Film Festival, which will run from Nov. 17 to 26.
                              </p>
                              <h3> QCinema’s opening film </h5>
                              <p><img class="img-fluid" src="https://entertainment.inquirer.net/files/2022/11/MOVIE-POSTERS.png" alt="Photo of Dolly Deleon, Soliman Cruz, and Chai Fonacier" class="f-left" />
                              <p> Swedish filmmaker Ruben Ostlund’s “Triangle of Sadness,” which features our very own Dolly de Leon, is QCinema’s opening film. It will be screened on Nov. 17, 6 p.m., at Gateway Cineplex. </p>
                              
                              <p>De Leon said she hoped local productions would also strictly implement an eight-hour work period during film shoots, “since health is a priority.”</p>
                              
                              <p>De Leon added that she appreciated “that each team member is looking out for each other, that there is collaboration between the filmmaker and the rest of the team, that each and every member has a voice and is respected for it. </p>
                              
                              <p>She likewise admired how “actors who are off-cam supported those who are on-cam, and that artists work around thought-provoking themes rather than tried-and-tested formulas. I also liked that they have full government support in terms of funding.” </p>
                              
                              <p>Meanwhile, Chai Fonacier got to work in Ireland for Lorcan Finnegan’s psychological-thriller “Nocebo” alongside Hollywood actors Eva Green and Mark Strong. </p>
                              
                              <p>“I was quite envious of the fact that film and TV workers in Ireland have unions that provide them some level of protection from abusive practices or less than ideal working conditions,” Fonacier began. “There have been times in my work experience in our country where I’ve been directly or indirectly told that the little things I ask for, or ask about would be deemed as ‘diva behavior.’ I used to be enraged about this.” </p>
                              
                              <p>Fonacier, who plays a Filipino caregiver who knows traditional folk healing in “Nocebo,” added: “I still get mad—and I think we have every right to be—but I’d like to believe I now have a more nuanced understanding of this. For so long, we have been made to feel powerless in demanding for better work conditions or better pay. The idea that suffering these work conditions equates to hard work has been so ingrained in us. It’s sad. </p> 
