                            <h1 class="mb-3 text-secondary text-uppercase font-weight-bold">The Metropolitan Waterworks and Sewerage System approves the water rate hikes, which will be implemented from 2023 to 2027</h1>
                            <p> MANILA, Philippines – Manila Water Company and Maynilad Water Services got the nod from state regulators to charge higher water bills for the next five years.
                            </p>

                            <p>The Metropolitan Waterworks and Sewerage System (MWSS) approved the rate hike requests of the two water concessionaires, which would be implemented from 2023 to 2027.
                            </p>

							<p> For Manila Water, the approved upward adjustments are as follows:
                            <ul>
                                <li>2023- P8.04 per cubic meter</li>
                                <li>2024- P5 per cubic meter</li>
                                <li>2025- P3.25 per cubic meter</li>
                                <li>2026- P3 per cubic meter</li>
                                <li>2027- P1.08 per cubic meter</li>
                            </ul>
                            Below is the expected upward adjustment per household consumption in 2023:
                            <ul>
                                <li>10 cubic meters- P41.19</li>
                                <li>20 cubic meters- P91.53</li>
                                <li>30 cubic meters- P187.01</li>
                            </ul>
                            Low income households consuming 10 cubic meters or less will see a P0.98 decrease in monthly bills since they are not covered by the inflation adjustment.
                            
                            </p>
                            
							<p> Manila Water’s environmental charge will also go up from 20% to 25% in 2023, and increase further to 30% starting 2026. The sewer charge was increased from 30% to 32.85% starting 2023, subject to Manila Water’s attainment of sewer coverage by the end of 2025.Speaking of standards, we asked Cruz to share some of the best practices he has observed while shooting the film in Romania that he hoped would also be done locally. To this, Cruz said: “Rehearsals prior to shooting is important to them. It’s when they study the script, it’s structure and dramaturgy. We rehearsed for a week. Another week was allotted for rehearsals with the cameras and lights. When it came to the actual shoot, everything went smoothly. As soon as we’re done for the day, it was already vodka time!” 
                            
                            </p>

							<p>The upward rate adjustment will fund Manila Water’s operational requirements amounting to P180 billion in the next five years.
                            </p>

                            <p>Manila Water services the east zone, which covers 1,400 square kilometers or 23 cities and municipalities in Metro Manila and Rizal. 

                            </p>
                            <p>These include Mandaluyong, Makati, Pasig, Pateros, San Juan, Taguig, Marikina, and parts of Quezon City and Manila. The towns of Angono, Baras, Binangonan, Cainta, Cardona, Jalajala, Morong, Pililia, Rodriguez, Tanay, Taytay, Teresa, San Mateo, and Antipolo in the province of Rizal are also part of the East Zone. 

                            </p>
                            <p>
                                For Maynilad, MWSS approved the following increases:
                                <ul>
                                    <li>2023- P3.29 per cubic meter</li>
                                        <li>    2024- P6.26 per cubic meter</li>
                                            <li>    2025- P2.12 per cubic meter</li>
                                                <li>    2026 and 2027- P1.01 per cubic meter</li>
                                </ul>
                                Maynilad’s environmental charge will also increase from 20% to 25% starting 2025, subject to Maynilad’s attainment of sewer coverage of 25% by the end of 2024.
                            </p>
                            <p>
                                On a per household consumption basis, the following increases are expected in 2023:
                                <ul>
                                    <li>10 cubic meters- P5.28</li>
                                        <li>    20 cubic meters- P20.29</li>
                                            <li>    30 cubic meters- P41.71</li>
                                </ul>
                                The upward rate adjustment will fund Maynilad’s P150-billion expansion plan for the next five years.
                            </p>
                            <p>
                                Maynilad services the west zone, which includes Manila (all but portions of San Andres and Sta. Ana), Quezon City (west of San Juan River, West Avenue, EDSA, Congressional, Mindanao Avenue, the northern part starting from the Districts of Holy Spirit and Batasan Hills), Makati (west of South Super Highway), Caloocan, Pasay, Parañaque, Las Piñas, Muntinlupa, Valenzuela, Navotas, and Malabon; as well as the cities of Cavite, Bacoor and Imus, and the towns of Kawit, Noveleta and Rosario, in Cavite.
                            </p>
                            <p>
                                “These rate adjustments will enable Manila Water and Maynilad to provide the highest quality of water, sanitation, and sewerage services that their customers deserve. These include the development of additional water sources that will ensure continuous improvements in the supply of safe
drinking water as well as the expansion of environmentally safe wastewater services within the concession areas,” MWSS said.
                            </p>
