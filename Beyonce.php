                            <h1 class="mb-3 text-secondary text-uppercase font-weight-bold">Beyoncé Leads Grammy Nominations with 9, Ties Record </h1>
                            
							<p>Los Angeles — Beyoncé has propelled herself into the highest Grammy echelon: The singer claimed a leading nine nominations Tuesday, making her tied — with her husband, Jay-Z — as the most nominated music act in the history of the awards show. <p>

                            <p>Beyoncé's "Break My Soul" reeled in record and song of the year nominations, while "Renaissance," which ventured into the world of dancehall music, netted an album of the year nod. With Jay-Z also earning five nods this year, each spouse now holds the record for the most-ever Grammy nominations at 88 apiece. <p>
							
							<p>Kendrick Lamar came away with the second-most nominations, with eight. Adele and Brandi Carlile both received seven nods. Harry Styles, Mary J. Blige, Future, DJ Khaled, The-Dream and mastering engineer Randy Merrill each picked up six. <p>
							

							<p> Nearly half of this year's leading nominees are women and more than half are people of color, according to the recording academy. The ceremony will be held February 5 in Los Angeles. <p>
							
							<p>"This makes me feel very proud, but it makes me conscious of the fact that we have to maintain the work we have done," said Harvey Mason jr., the Recording Academy's CEO. He said there have been strides in the peer-driven voting system and increased membership, but he still believes more progress can be made. <p>
							
							
							<p>"We have almost 13,000 voters now." Mason said. … "I'm pleased to think they spent the time listening to the music and evaluating. I think you see by the type of nominations that they are not only going for just popular music or music that has a lot of streams. It's just music of high quality." <p>
							
							<p>The academy added a special song for social change and five new categories including songwriter of the year, which Harvey says will further help diversify the 65th edition of the annual awards. <p>
							
						
							<p>The non-classical songwriter category will recognize one individual who was the "most prolific" non-performing and non-producing songwriter for a body of new work during an eligibility year. It will take a different approach than song of the year, which awards the songwriters who wrote the lyrics or melodies to one song. <p>
							
							<p>Harvey said implementing the songwriter category is a significant step forward for the music industry. Last year, a rule update allowed that any songwriter, producer, engineer or featured artist on a work nominated for album of the year could ultimately earn a nomination. <p>
							
							<p>"The academy and voters are placing a high importance on the craft of songwriting," Harvey said of the new category, in which nominees include The-Dream, Amy Allen, Nija Charles, Tobias Jesso Jr. and Laura Veltz. <p>
							
							<p>Harvey said it's his priority to earn the trust from the music community including The Weeknd and Drake, who are still unwilling to submit any of their music. Drake declined to submit his album "Honestly Nevermind" for the awards, but the Canadian rapper still received four nominations, including album of the year for his co-writing efforts on Beyoncé's "Renaissance." <p>
							
							<p>Beyoncé, the most decorated woman in Grammy history with 28 wins, could break the late Hungarian-British conductor Georg Solti's record for most awards won if she wins four awards. Solti, who has 31 Grammys, has held on to the record since 1997. <p>
							
							<p>For the first time in Beyoncé's lauded career, she was nominated in the dance category. Her seventh studio project is up for best dance-electronic music album and "Break My Soul" is nominated for best dance-electronic recording. Other nominations include best R&B song for "Cuff It," R&B performance for "Virgo's Groove," traditional R&B performance for "Plastic Off the Sofa" and song written for visual media for "Be Alive," the Oscar-nominated song from the "King Richard" soundtrack. <p>
							
							<p>Other album of the year nominees include: Adele's "30," ABBA's "Voyage," Bad Bunny's "Un Verano Sin Ti," Mary J. Blige's "Good Morning Gorgeous" (Deluxe), Carlile's "In These Silent Days," Coldplay's "Music of the Spheres," Lamar's "Mr. Morale & the Big Steppers," Lizzo's "Special" and Styles' "Harry's House." Bad Bunny made history, with his album becoming the first sung entirely in Spanish to compete in the category. <p>
							
							<p>Tracks competing with "Break My Soul" for record of the year include Styles' "As It Was," Doja Cat's "Woman," Adele's "Easy On Me," ABBA's "Don't Shut Me Down," Blige's "Good Morning Gorgeous," Steve Lacy's "Bad Habit," Lamar's "The Heart Part 5," Lizzo's "About Damn Time" and Carlile's "You and Me On the Rock" featuring Lucius. <p>
							
							<p>Three of Jay-Z's nominations came through DJ Khaled's "God Did," a song featuring Rick Ross, Lil Wayne, John Legend and Fridayy. The track is up for best rap performance and rap song along with song of the year, which also has Jay-Z nominated for his writing efforts on Beyoncé's "Break My Soul." The rapper also received a nod for album of the year for his work on his wife's "Renaissance" album. <p>
							
							<p>Young Thug and Gunna both received nominations despite being currently locked up in a racketeering criminal case. The rappers are up for best rap song and rap performance through Gunna's "pushin P," featuring Thug and Future. <p>
							
							<p>Christina Aguilera's comeback to Latin music with her self-titled album earned her two nominations for best Latin pop album and immersive audio album. Among the 10 nominees for best new artist are Muni Long, Latto and Eurovision winner Maneskin. <p>

