                            <h1 class="mb-3 text-secondary text-uppercase font-weight-bold">SSS starts offering calamity assistance to Paeng-affected members, pensioners
                            </h1>
                            <p>
                                State-run private sector pension fund Social Security System (SSS) on Thursday began the financial assistance program to members and pensioners in areas affected by Severe Tropical Storm Paeng.</p>
                            <p>
                                The offering of the financial aid program for Paeng-hit members and pensioners started on November 17, 2022 and will last until February 16, 2023
                            </p>
                            <p>In a statement, SSS president and CEO Michael Regino said that the SSS Calamity Assistance Package consists of the Calamity Loan Assistance Program (CLAP) for its members and the Three-Month Advance Pension for SSS and Employees’ Compensation (EC) pensioners.

                            </p>
                            <p>“We, at SSS, are always ready to assist our members and pensioners in the typhoon-stricken areas. We want to assure them that even during times of calamities, they can rely on the financial assistance from SSS,” Regino said.
                            </p>
                            <p>
                                The SSS chief said members and pensioners in the areas to be declared under state of calamity by the National Disaster Risk Reduction and Management Council (NDRRMC), or the Sangguniang Bayan, Panglungsod, or Panlalawigan can avail of the financial assistance.
                            </p>
                            <p>
                                Provinces under state of calamity are Cavite, Laguna, Batangas, Rizal, Quezon, Albay, Camarines Norte, Camarines Sur, Sorsogon, Catanduanes, Masbate, Aklan, Antique, Capiz, Guimaras, Iloilo, Negros Occidental, Basilan, Lanao del Sur, Tawi-Tawi, Sulu, Maguindanao del Norte and Maguindanao del Sur.
                            </p>
                            <p>In Cagayan, the towns that are under the state of calamity include Amulung, Enrile, and Tuguegarao. In Marinduque, these areas are Mogpog, Santa Cruz and Buenavista, and in Cotabato, these are Pigcawayan, Libungan, and Midsayap. Other areas also declared under state of calamity include Muntinlupa City, Calbayog in Samar, Sirawai in Zamboanga del Norte, and Zamboanga City in Zamboanga del Sur.
                            </p>
                            <p>
                                Under the CLAP, qualified members in Paeng-affected areas can borrow a loan equivalent to their one monthly salary credit or up to P20,000.
                            </p>
                            <p>
                                The calamity loan is payable in two years or 24 equal monthly installments with an annual interest rate of 10%.
                            </p>
                            <p>
                                Likewise, the SSS will provide financial aid to SSS and EC pensioners in typhoon-hit areas.
                            </p>
                            <p>
                                “Pensioners may avail of the Three-Month Advance Pension wherein SSS pensioners will be given three months advance of their total monthly pension,” Regino said.
                            </p>
                            <p>
                                “We want to help our members and pensioners during these difficult times. We hope that the financial aid that we extended to them will be of big help to rebuild their lives,” Regino said.
                            </p>
                            <p>
                                The SSS chief reminded members and pensioners in areas affected by Super Typhoon Karding that they still have until January 6, 2023 to avail of the CLAP and Three-Month Advance Pension for those hit by the super typhoon.
                            </p>
        
