                            <h1 class="mb-3 text-secondary text-uppercase font-weight-bold">Local, international highlights at QCinema </h1>
                            
							<p>The QCinema International Film Festival kicks off on November 17 and will run until November 26.<p>

                            <p>Among its highlights are the premiere of the digitally-restored version of the film classic "Itim" and the Philippine premiere of the provocative thriller "Bones and All."<p>

							<p><b>Mike de Leon's "Itim"</b><p>

							<p> Filipinos can watch the digitally-restored version of the revered Filipino classic film "Itim" (The Rites of May) by Mike De Leon, which earned Charo Santos her first Best Actress award at the 1978 Asian Film Festival.<p>
							
							<p>The 1981 Gawad Urian's Best Film of the Decade is one of the main titles in QCinema's Digitally Restored Classics category.<p>
							
							
							<p>Since its release in 1976, the film continues to gain recognition from international audiences to this date â€” most recently being selected as one of the classics to be screened at the world's prestigious Cannes Film Festival last May 2022.<p>
							
							<p>Itim was the first film directed by acclaimed filmmaker de Leon and the first film in which Santos played the lead role. It also starred seasoned actors Tommy Abuel, Mario Montenegro, Mona Lisa, and Susan Valdez.<p>
							
						
							<p>In an interview, Santos described working with de Leon, whom she considers one of her dearest mentors throughout her career as an actress and a producer in the film industry.<p>
							
							<p>Santos said, "There was mutual respect. I respect his artistry. I admire him because he has a handle on his narrative and understands the film language."<p>
							
							<p>"He's very disciplined. Everything I learned about filmmaking, I learned from Mike," shared Santos, who was discovered and asked by award-winning director Lino Brocka to audition for the de Leon's first directorial job.<p>
							
							<p>Itim retells the story of a budding photographer Jun (Abuel), and how he documented the locals' Lenten rituals during his return to his provincial hometown. He then met and befriended a mysterious woman named Teresa (Santos). But as soon as she opened up to him about her life, she was suddenly possessed by the spirit of her sister Rosa (Valdez), whose untimely death was unknown to their family. Through her sister's restless soul, Teresa recounts the unsolved mystery behind Rosa's disappearance.<p>
							
							
							<img class="img-fluid w-100" src="https://cdn4.premiumread.com/?url=https://www.manilatimes.net/manilatimes/uploads/images/2022/11/15/126865.jpg&w=700&q=100&f=webp" cover;">
							
							<p><b>Luca Guadagnino's "Bones and All"</b><p>
							
							<p>MGM Pictures and Warner Bros.' highly anticipated provocative thriller "Bones and All" from director Luca Guadagnino ("Call Me By Your Name") will also premiere at the 10th QCinema International Film Festival.<p>
							
							<p>The award-winning film starring Timothee Chalamet, Taylor Russell and Mark Rylance will have festival screenings on November 18 and 19, at 8 p.m., in The Block Cinemas, SM North EDSA.<p>
							
							<p>Bones and All is a story of first love and a liberating road odyssey of two young people (Chalamet, Russell) coming into their own, searching for identity and chasing beauty in a perilous world that cannot abide who they are.<p>
							
							<p>In September, the film held its world premiere in the 79th Venice International Film Festival where it received over eight minutes of standing ovation. During the festival's awards night, Luca Guadagnino was honored with the Silver Lion for Best Director, and the Marcello Mastroianni Award for Best Young Actress went to Taylor Russell.<p>
							
							<p>With an impressive 88 percent Tomatometer score at reviews-aggregator site Rotten Tomatoes, "Bones and All" has become one of the most critically-acclaimed films of the year.<p>
							
							<p>In its review, The Hollywood Reporter wrote "Guadagnino's seemingly divergent interests in romance and horror have never come together quite so ideally as they do here, played out against a constantly moving canvas of small-town America."<p>
							
							<p>"Its actors give you something to watch every minute," praised TIME Magazine, while Entertainment Weekly described the film as "A born provocateur's faithful ode to a classic cinematic genre, only with human gristle between its teeth."<p>
							
							<p>Finally, Los Angeles Times raved "There's real pleasure in `Bones and All,' an insistent sweetness that somehow both nourishes and cleanses away the horror."<p>
