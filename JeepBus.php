                            <h1 class="mb-3 text-secondary text-uppercase font-weight-bold">Jeepney, bus groups ask for rush-hour surcharge</h1>
                            
							<p>MANILA, Philippines — As if the lack of public transportation, particularly during rush hour, wasn't bad enough, weary commuters may have to pay more when taking buses and jeepneys to and from work.<p>

                            <p>Tuesday marked the start of the Land Transportation <a href="https://ltfrb.gov.ph/">Franchising and Regulatory Board's</a> (LTFRB) hearing on a petition submitted by transportation organizations last month asking for a surge in fares for jeepneys and buses during rush hours due to rising fuel costs.<p>

							<p> Transport organizations requested that the LTFRB allow traditional and modern jeepneys to charge every passenger an extra P1, and P2 for public buses, during the rush hour periods from 5-8 a.m. in a 42-page petition submitted on October 14. except on Sundays and federal holidays, and 4–8 p.m. <p>

							<p>Surge pricing will be in addition to the base or minimum fares for the various modes of transportation. The minimum fare was raised last September by P1 to P12 for the first four kilometers for traditional jeepneys and to P14 for contemporary jeepneys after the LTFRB approved a petition for a fare increase.<p>

							<p>In Metro Manila, the base fare for public transportation increased by P2 for the first five kilometers, going from P11 for regular provincial buses to P13 for regular and P15 for air conditioning. Taxi and ride-hailing app flag-down rates both increased by P5 (transport network vehicle services, or TNVS).<p>
							
							<p>Pasang Masda, the Alliance of Concerned Transport Organizations, and the Alliance of Transport Operators and Drivers Association of the Philippines filed the petition calling for a rush-hour surcharge.<p>
							
							<p>Hearing to follow in 2023<p>
							
							<p>The transport groups' private attorney, Nichelle Ann Cabog-Bergonia, stated that during the hearing on Tuesday, they asked the LTFRB for a 30-day window to provide a memorandum stating their stance.<p>
							
							<p>The next hearing would take place on February 2 of the following year, but no witnesses or new evidence would be offered, according to the attorney.<p>
							
							<p>The transportation advocacy groups claimed in their surcharge petition that the most recent fare increases were "not enough to ease the hardships of public utility vehicle operators in the face of further fuel price hikes, higher operational costs due to inflation, and also due to rush congestion in the city."<p>
							
							<p>The LTFRB announced it will review the petition four days after the groups submitted it, acknowledging the request for a further rate hike from public utility drivers and operators.<p>
							
							<p>Jaime Bautista, the secretary of transportation, has also stated that the demand for a surge price for public transit was "reasonable," however he did not provide further details.<p>
							
							<p>The LTFRB earlier assured public utility vehicle (PUV) drivers and operators that their petition would be heard in the wake of the continued rise of fuel prices.<p>
							
							<p>It said it understood the arguments raised by the transport groups, especially the concern to increase the earning of drivers and conductors.<p>
							
							<p>In hearing the new petition of transport groups for a rush-hour surcharge, however, the LTFRB noted that the plight of commuters must also be heard, especially after the recent fare increases.<p>
							
							<p><b>Shrinking income</b><p>
							
							<p>The steep increase in prices of gasoline and diesel had eaten into the earnings of PUV drivers and operators, many of them choosing to look for other work or additional jobs to increase their income.<p>
							
							<p>Even those outside Metro Manila have complained about the impact of rising fuel prices on their livelihood.<p>
							
							<p>Jeepney drivers in Luzon provinces, for instance, are struggling to bring home enough money for their families after a day on the road due to rising fuel prices, forcing them to take on odd jobs and find other ways to cope with the crisis.<p>
							
							<p>Cris Mira, chair of the transport group Kaisahan ng mga Sasakyang Namamasada sa Bataan (Kasakbayan), said many jeepney drivers were feeling the crunch as they found it hard to pay their operators under the traditional boundary (daily lease) scheme.<p>
							
							<p>“They (drivers) would be lucky if they could pay a boundary of P300. Their supposed take-home income is spent on fuel,” Mira told the Inquirer in an interview on Tuesday.<p>
							
							<p>He said many of his group’s members had been forced to find temporary jobs on construction sites.<p>

